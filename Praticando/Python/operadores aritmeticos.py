

#| Operador | Nome                          | Exemplo  | Resultado |
#| -------- | ----------------------------- | -------- | --------- |
#| `+`      | **Adição**                    | `5 + 3`  | `8`       |
#| `-`      | **Subtração**                 | `5 - 3`  | `2`       |
#| `*`      | **Multiplicação**             | `5 * 3`  | `15`      |
#| `/`      | **Divisão real**              | `5 / 2`  | `2.5`     |
#| `//`     | **Divisão inteira**           | `5 // 2` | `2`       |
#| `%`      | **Módulo (resto da divisão)** | `5 % 2`  | `1`       |
#| `**`     | **Exponenciação (potência)**  | `5 ** 2` | `25`      |
